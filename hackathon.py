import pandas as pd
import numpy as np
from sklearn.preprocessing import MinMaxScaler, Normalizer
from sklearn.decomposition import TruncatedSVD
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.impute import SimpleImputer

def generate_survey_embeddings(labels_files, numbers_files, output_dim=64):
    """
    Integrates 8 survey files into a unified latent embedding space.
    """
    all_num_dfs = []
    all_lab_dfs = []

    print("Cleaning and loading files...")
    for nf, lf in zip(numbers_files, labels_files):
        # 1. Load and Skip Metadata Rows (Row 0: Text, Row 1: Qualtrics IDs)
        df_num = pd.read_csv(nf).iloc[2:]
        df_lab = pd.read_csv(lf).iloc[2:]

        # 2. Clean and Index by person_id
        for df in [df_num, df_lab]:
            df.dropna(subset=['person_id'], inplace=True)
            df['person_id'] = df['person_id'].astype(str).str.strip()
            df.set_index('person_id', inplace=True)

        # 3. Numeric Path Cleaning: Force numbers and handle 'inf'
        df_num = df_num.apply(pd.to_numeric, errors='coerce')
        df_num = df_num.replace([np.inf, -np.inf], np.nan)

        # 4. Feature Selection: Drop columns with >95% missing values
        df_num = df_num.dropna(axis=1, thresh=int(0.05 * len(df_num)))

        # Prefix columns to avoid collisions between surveys
        survey_id = nf.split('_')[1]
        df_num.columns = [f"s{survey_id}_{c}" for c in df_num.columns]
        df_lab.columns = [f"s{survey_id}_{c}" for c in df_lab.columns]

        all_num_dfs.append(df_num)
        all_lab_dfs.append(df_lab)

    # 5. Harmonize: Merge all surveys into Master Matrices
    df_num_master = pd.concat(all_num_dfs, axis=1).dropna(how='all')
    df_lab_master = pd.concat(all_lab_dfs, axis=1).reindex(df_num_master.index)

    print(f"Generating embeddings for {len(df_num_master)} respondents...")

    # PATH A: Numerical Latent Representation
    imputer = SimpleImputer(strategy='median')
    X_num = imputer.fit_transform(df_num_master)
    scaler = MinMaxScaler()
    X_num_scaled = scaler.fit_transform(X_num)

    svd_num = TruncatedSVD(n_components=output_dim, random_state=42)
    emb_num = svd_num.fit_transform(X_num_scaled)

    # PATH B: Semantic Label Representation
    text_data = df_lab_master.fillna('').astype(str).apply(lambda row: ' '.join(row), axis=1)
    vectorizer = TfidfVectorizer(max_features=1000, stop_words='english')
    X_text = vectorizer.fit_transform(text_data)

    svd_text = TruncatedSVD(n_components=output_dim, random_state=42)
    emb_text = svd_text.fit_transform(X_text)

    # FUSION: Concatenate and Normalize
    fusion = np.hstack((emb_num, emb_text))
    normalizer = Normalizer()
    final_embeddings = normalizer.fit_transform(fusion)

    # Convert to DataFrame
    cols = [f'embedding_dim_{i}' for i in range(final_embeddings.shape[1])]
    result_df = pd.DataFrame(final_embeddings, columns=cols, index=df_num_master.index)

    return result_df

# List your files in pairs
labels = ['survey_1_labels.csv', 'survey_2_labels.csv', 'survey_3_labels.csv', 'survey_4_labels.csv']
numbers = ['survey_1_numbers.csv', 'survey_2_numbers.csv', 'survey_3_numbers.csv', 'survey_4_numbers.csv']

# Generate and save
embeddings = generate_survey_embeddings(labels, numbers)
embeddings.to_csv('final_survey_embeddings.csv')
