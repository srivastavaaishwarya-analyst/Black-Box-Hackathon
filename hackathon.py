def get_text_embedding(df, text_col):
    """
    For a given text column in a survey dataframe, compute a spaCy
    document-level embedding (mean of word vectors) for each respondent.
    Returns a DataFrame with 300 columns: text_embed_0 ... text_embed_299.
    Rows with missing/empty text get a zero vector.
    """
    texts = df[text_col].fillna("").astype(str).tolist()

    vectors = []
    for doc in nlp.pipe(texts, batch_size=50):   # nlp.pipe is faster than a plain loop
        vectors.append(doc.vector)               # 300-dim averaged word vector

    embed_df = pd.DataFrame(
        vectors,
        columns=[f'text_embed_{i}' for i in range(300)],
        index=df.index
    )
    return embed_df


def compress_text_embeds(embed_df, n_components=5, prefix='txt'):
    """
    Reduce 300-dim text embeddings to n_components via PCA.
    This prevents the 300 raw dimensions from drowning out the
    21 subscale features in the main feature matrix.
    """
    pca_text = PCA(n_components=n_components, random_state=42)
    compressed = pca_text.fit_transform(
        StandardScaler().fit_transform(embed_df)
    )
    return pd.DataFrame(
        compressed,
        columns=[f'{prefix}_pc{i+1}' for i in range(n_components)],
        index=embed_df.index
    )


