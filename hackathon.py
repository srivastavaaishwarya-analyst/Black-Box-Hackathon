def subscale_mean(df, prefix):
    """
    Average all columns whose name starts with `prefix` into a single score.
    Converts to numeric, coercing non-numeric to NaN before averaging.
    Returns a Series aligned to df's index.
    """
    cols = [c for c in df.columns if c.startswith(prefix)]
    if not cols:
        return None
    numeric = pd.to_numeric(
        df[cols].stack(future_stack=True), errors='coerce'
    ).unstack()
    return numeric.mean(axis=1)


